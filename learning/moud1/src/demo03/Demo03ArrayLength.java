package demo03;

public class Demo03ArrayLength {

    public static void main(String[] args) {
        // 给数组赋值
        int[] arrayA = new int[10];
        arrayA[0] = 123;
        arrayA[1] = 12;
        // 数组长度
        int len = arrayA.length;
        System.out.println("长度为：" + len);


    }

}
